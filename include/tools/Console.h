#pragma once

void ConsoleWriteLineStr(const char * value)
{
    printf("%s\n", value);
}
void ConsoleWriteLineInt(const int value)
{
    printf("%i\n", value);
}
void ConsoleWriteLineLong(const long value)
{
    printf("%li\n", value);
}
void ConsoleWriteLineFloat(const float value)
{
    printf("%f\n", value);
}